// ── Provider Abstraction ──────────────────────────────────────────────────────
public interface IChatCompletionProvider
{
    IAsyncEnumerable<string> StreamAsync(string prompt, List<AIChatMessage> history, CancellationToken ct);
    string ModelName { get; }
}

// ── Smart Mock Provider (production-safe, no external API key needed) ─────────
public class SmartMockProvider : IChatCompletionProvider
{
    public string ModelName => "mock-v1";

    public async IAsyncEnumerable<string> StreamAsync(
        string prompt,
        List<AIChatMessage> history,
        [EnumeratorCancellation] CancellationToken ct)
    {
        var responses = new[]
        {
            $"Analyzing your request: **\"{TruncatePrompt(prompt)}\"**\n\n",
            "Based on the platform data available, here's my assessment:\n\n",
            BuildContextualResponse(prompt),
            "\n\n---\n*AI response generated by SessionFlow Neural Engine (Mock Mode)*"
        };

        foreach (var chunk in responses)
        {
            ct.ThrowIfCancellationRequested();
            await Task.Delay(80, ct);
            yield return chunk;
        }
    }

    private static string TruncatePrompt(string prompt) =>
        prompt.Length > 60 ? prompt[..60] + "..." : prompt;

    private static string BuildContextualResponse(string prompt)
    {
        var lower = prompt.ToLowerInvariant();
        if (lower.Contains("session") || lower.Contains("attendance"))
            return "📊 **Session & Attendance Insights:**\n- Track session completion rates via the Analytics dashboard\n- Use the Session Timeline to identify patterns in student engagement\n- Automated reminders are sent 30 mins before each session";
        if (lower.Contains("student"))
            return "👥 **Student Management Tips:**\n- Use bulk enrollment for faster onboarding\n- Student performance scores update after each session\n- Export reports in CSV/PDF from the Reporting module";
        if (lower.Contains("broadcast") || lower.Contains("message"))
            return "📣 **Broadcast System:**\n- You can target by role: Admin, Engineer, or Student\n- Scheduled broadcasts support future-dated delivery\n- Track open rates in Broadcast History";
        if (lower.Contains("report") || lower.Contains("analytics"))
            return "📈 **Analytics Overview:**\n- DAU/WAU/MAU trends are tracked automatically\n- Feature engagement shows which pages users visit most\n- Session metrics include average duration and peak hours";
        return "✅ I'm here to help with platform management, session tracking, analytics, and user insights. What specific data would you like to explore?";
    }
}

// ── OpenAI Provider (real implementation, activated by config) ─────────────
public class OpenAIProvider : IChatCompletionProvider
{
    private readonly string _apiKey;
    private readonly string _model;
    private readonly HttpClient _http;
    public string ModelName => _model;

    public OpenAIProvider(HttpClient http, string apiKey, string model = "gpt-4o-mini")
    {
        _http = http;
        _apiKey = apiKey;
        _model = model;
    }

    public async IAsyncEnumerable<string> StreamAsync(
        string prompt,
        List<AIChatMessage> history,
        [EnumeratorCancellation] CancellationToken ct)
    {
        // Build message array
        var messages = new List<object>
        {
            new { role = "system", content = "You are a helpful AI assistant for SessionFlow, an educational SaaS platform. Be concise, professional, and helpful." }
        };
        foreach (var h in history.TakeLast(10))
            messages.Add(new { role = h.Role, content = h.Content });
        messages.Add(new { role = "user", content = prompt });

        var body = JsonSerializer.Serialize(new
        {
            model = _model,
            messages,
            stream = true,
            max_tokens = 1024
        });

        using var req = new HttpRequestMessage(HttpMethod.Post, "https://api.openai.com/v1/chat/completions");
        req.Headers.Authorization = new System.Net.Http.Headers.AuthenticationHeaderValue("Bearer", _apiKey);
        req.Content = new StringContent(body, Encoding.UTF8, "application/json");

        using var resp = await _http.SendAsync(req, HttpCompletionOption.ResponseHeadersRead, ct);
        resp.EnsureSuccessStatusCode();

        using var stream = await resp.Content.ReadAsStreamAsync(ct);
        using var reader = new StreamReader(stream);

        while (!reader.EndOfStream && !ct.IsCancellationRequested)
        {
            var line = await reader.ReadLineAsync(ct);
            if (string.IsNullOrEmpty(line) || !line.StartsWith("data: ")) continue;
            var data = line["data: ".Length..];
            if (data == "[DONE]") break;

            JsonDocument? doc = null;
            try { doc = JsonDocument.Parse(data); } catch { continue; }
            using (doc)
            {
                var content = doc.RootElement
                    .GetProperty("choices")[0]
                    .GetProperty("delta")
                    .TryGetProperty("content", out var c) ? c.GetString() : null;
                if (content != null) yield return content;
            }
        }
    }
}

