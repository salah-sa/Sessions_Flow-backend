// ── Main AI Service ───────────────────────────────────────────────────────────
public class AIService
{
    private readonly IChatCompletionProvider _provider;
    private readonly MongoService _db;
    private readonly IConnectionMultiplexer? _redis;
    private readonly ILogger<AIService> _logger;
    private readonly IConfiguration _config;

    // Quota per tier (requests per 3-hour window)
    private const int QuotaWindowHours = 3;
    public static readonly Dictionary<string, int> TierQuotas = new()
    {
        ["Free"] = 5,
        ["Pro"] = 90,
        ["Ultra"] = 180,
        ["Enterprise"] = int.MaxValue
    };

    public AIService(
        IChatCompletionProvider provider,
        MongoService db,
        IConnectionMultiplexer? redis,
        ILogger<AIService> logger,
        IConfiguration config)
    {
        _provider = provider;
        _db = db;
        _redis = redis;
        _logger = logger;
        _config = config;
    }

    /// <summary>Check Redis cache, then call provider, then log to MongoDB.</summary>
    public async IAsyncEnumerable<string> ChatStreamAsync(
        Guid userId,
        string userRole,
        string sessionId,
        string prompt,
        List<AIChatMessage> history,
        string userTier = "Free",
        [EnumeratorCancellation] CancellationToken ct = default)
    {
        var cacheKey = BuildCacheKey(prompt, history);
        var cacheDb = _redis?.GetDatabase();

        // ── 1. Quota Check (3-hour window) ────────────────────────────────
        if (!await CheckQuotaAsync(userId, userTier, cacheDb))
        {
            yield return "⚠️ **Quota reached.** You've used all your messages for this 3-hour window. Upgrade your plan or wait for the next window.";
            yield break;
        }

        // ── 2. Cache Hit ────────────────────────────────────────────────────
        string? cached = null;
        if (cacheDb != null)
        {
            cached = await cacheDb.StringGetAsync(cacheKey);
        }

        if (!string.IsNullOrEmpty(cached))
        {
            _logger.LogDebug("[AI] Cache HIT for prompt hash {Hash}", cacheKey[^12..]);
            yield return cached;
            await LogAsync(userId, userRole, sessionId, prompt, cached, wasCached: true);
            yield break;
        }

        // ── 3. Stream from provider & accumulate ────────────────────────────
        var sb = new StringBuilder();
        var sw = System.Diagnostics.Stopwatch.StartNew();
        var wasError = false;
        string? errorMsg = null;

        await foreach (var chunk in _provider.StreamAsync(prompt, history, ct))
        {
            sb.Append(chunk);
            yield return chunk;
        }

        sw.Stop();
        var fullResponse = sb.ToString();

        // ── 4. Cache the response (TTL: 1 hour) ────────────────────────────
        if (cacheDb != null && !string.IsNullOrEmpty(fullResponse))
        {
            await cacheDb.StringSetAsync(cacheKey, fullResponse, TimeSpan.FromHours(1));
        }

        // ── 5. Increment quota usage (3-hour window) ─────────────────────
        if (cacheDb != null)
        {
            var (quotaKey, ttl) = BuildQuotaKey(userId);
            await cacheDb.StringIncrementAsync(quotaKey);
            await cacheDb.KeyExpireAsync(quotaKey, ttl);
        }

        // ── 6. Persist log to MongoDB ───────────────────────────────────────
        await LogAsync(userId, userRole, sessionId, prompt, fullResponse,
            durationMs: (int)sw.ElapsedMilliseconds,
            wasCached: false, wasError: wasError, errorMessage: errorMsg);
    }

    private async Task<bool> CheckQuotaAsync(Guid userId, string tier, IDatabase? db)
    {
        if (db == null) return true;
        var limit = TierQuotas.GetValueOrDefault(tier, 5);
        if (limit == int.MaxValue) return true;

        var (quotaKey, _) = BuildQuotaKey(userId);
        var current = (long?)await db.StringGetAsync(quotaKey) ?? 0;
        return current < limit;
    }

    /// <summary>Builds a Redis key scoped to a 3-hour bucket from Unix epoch.</summary>
    private static (string key, TimeSpan ttl) BuildQuotaKey(Guid userId)
    {
        var now = DateTimeOffset.UtcNow;
        var bucket = now.ToUnixTimeSeconds() / (QuotaWindowHours * 3600);
        var key = $"ai:quota:{userId}:{bucket}";
        // TTL = time remaining until this 3h window ends
        var windowStart = DateTimeOffset.FromUnixTimeSeconds(bucket * QuotaWindowHours * 3600);
        var windowEnd = windowStart.AddHours(QuotaWindowHours);
        var ttl = windowEnd - now;
        if (ttl < TimeSpan.FromSeconds(10)) ttl = TimeSpan.FromHours(QuotaWindowHours);
        return (key, ttl);
    }

    private async Task LogAsync(
        Guid userId, string userRole, string sessionId,
        string prompt, string response,
        int durationMs = 0, bool wasCached = false,
        bool wasError = false, string? errorMessage = null)
    {
        try
        {
            var log = new AILog
            {
                UserId = userId,
                UserName = userRole,
                SessionId = sessionId,
                Prompt = prompt,
                Response = response,
                Model = _provider.ModelName,
                DurationMs = durationMs,
                WasCached = wasCached,
                WasError = wasError,
                ErrorMessage = errorMessage
            };
            await _db.AILogs.InsertOneAsync(log);
        }
        catch (Exception ex)
        {
            _logger.LogWarning(ex, "[AI] Failed to persist AI log");
        }
    }

    // ── Prompt Preset Management ─────────────────────────────────────────────
    public async Task<List<AIPromptPreset>> GetPresetsAsync(Guid userId)
    {
        var filter = Builders<AIPromptPreset>.Filter.Or(
            Builders<AIPromptPreset>.Filter.Eq(p => p.UserId, userId),
            Builders<AIPromptPreset>.Filter.Eq(p => p.IsSystemPreset, true)
        );
        return await _db.AIPromptPresets.Find(filter)
            .SortByDescending(p => p.UsageCount)
            .ToListAsync();
    }

    public async Task<AIPromptPreset> CreatePresetAsync(Guid userId, AIPresetRequest req)
    {
        var preset = new AIPromptPreset
        {
            UserId = userId,
            Title = req.Title,
            Prompt = req.Prompt,
            Icon = req.Icon,
            Category = req.Category
        };
        await _db.AIPromptPresets.InsertOneAsync(preset);
        return preset;
    }

    public async Task DeletePresetAsync(Guid presetId, Guid userId)
    {
        await _db.AIPromptPresets.DeleteOneAsync(
            p => p.Id == presetId && p.UserId == userId && !p.IsSystemPreset);
    }

    public async Task<List<AILog>> GetLogsAsync(Guid? userId, int page = 1, int pageSize = 50)
    {
        var filter = userId.HasValue
            ? Builders<AILog>.Filter.Eq(l => l.UserId, userId.Value)
            : Builders<AILog>.Filter.Empty;

        return await _db.AILogs.Find(filter)
            .SortByDescending(l => l.Timestamp)
            .Skip((page - 1) * pageSize)
            .Limit(pageSize)
            .ToListAsync();
    }

    /// <summary>Returns real-time quota info for the current 3-hour window.</summary>
    public async Task<(int used, int limit, DateTimeOffset resetsAt)> GetQuotaInfoAsync(Guid userId, string tier, IDatabase? cacheDb)
    {
        var limit = TierQuotas.GetValueOrDefault(tier, 5);
        if (cacheDb == null) return (0, limit, DateTimeOffset.UtcNow.AddHours(QuotaWindowHours));

        var (quotaKey, _) = BuildQuotaKey(userId);
        var used = (int)((long?)await cacheDb.StringGetAsync(quotaKey) ?? 0);

        // Calculate when this window resets
        var now = DateTimeOffset.UtcNow;
        var bucket = now.ToUnixTimeSeconds() / (QuotaWindowHours * 3600);
        var windowEnd = DateTimeOffset.FromUnixTimeSeconds((bucket + 1) * QuotaWindowHours * 3600);

        return (used, limit, windowEnd);
    }

    /// <summary>Returns user's AI chat history from MongoDB, grouped by session.</summary>
    public async Task<List<AILog>> GetUserHistoryAsync(Guid userId, int limit = 100)
    {
        return await _db.AILogs.Find(
            Builders<AILog>.Filter.Eq(l => l.UserId, userId)
        )
        .SortByDescending(l => l.Timestamp)
        .Limit(limit)
        .ToListAsync();
    }

    private static string BuildCacheKey(string prompt, List<AIChatMessage> history)
    {
        var historyContext = string.Join("|", history.TakeLast(3).Select(h => $"{h.Role}:{h.Content[..Math.Min(50, h.Content.Length)]}"));
        var raw = $"{prompt.Trim()}___{historyContext}";
        var hash = SHA256.HashData(Encoding.UTF8.GetBytes(raw));
        return $"ai:cache:{Convert.ToHexString(hash)[..32].ToLower()}";
    }
}
